    private void buttPlusOrMinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttPlusOrMinActionPerformed
        // TODO add your handling code here:
        if(displayBox.getText().equals("") && displayInnerBox.getText().equals("")){
            displayBox.setText("");
            displayInnerBox.setText("");
            x = 0;
        }else {
            if (x == 0){
                displayBox.setText("-" + displayBox.getText());
                
                if (y == 1){
                    displayInner = displayInnerBox.getText();
                    switch(operasi){
                        case 'x'-> {
                            int index = displayInnerBox.getText().indexOf("x") + 1;
                            minus(index);
                            break;
                        }
                        case '/'-> {
                            int index = displayInnerBox.getText().indexOf("/") + 1;
                            minus(index);
                            break;
                        }
                        case '+'-> {
                            int index = displayInnerBox.getText().indexOf("+") + 1;
                            minus(index);
                            break;
                        }
                        case '-'-> {
                            int index = displayInnerBox.getText().indexOf("-") + 1;
                            minus(index);
                            break;
                        }
                    }
                } else {
                    displayInnerBox.setText(displayBox.getText());
                    y = 0;
                }
                x = 1;
            } else {
                displayBox.setText(displayBox.getText());
                displayInnerBox.setText(displayInnerBox.getText());
            }
        }
    }//GEN-LAST:event_buttPlusOrMinActionPerformed

